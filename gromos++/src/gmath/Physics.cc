// gmath_Physics.cc
#include "Physics.h"

const double gmath::pi = 3.14159265358979323846;
const double gmath::angstrom = 1e-10;
const double gmath::kilo = 1e3;
const double gmath::nano = 1e-9;
const double gmath::pico = 1e-12;
const double gmath::radian2degree = 180.0 / gmath::pi;
const double gmath::degree2radian = gmath::pi / 180.0;
const double gmath::calorie2joule = 4.184;
const double gmath::elementary_charge = 1.60217733e-19;
const double gmath::atomic_mass_unit = 1.6605402e-27;
const double gmath::boltzmann = 1.380658e-23;
const double gmath::avogadro = 6.0221367e23;
const double gmath::gas_constant = gmath::boltzmann * gmath::avogadro;
const double gmath::boltz = gmath::gas_constant / gmath::kilo;
const double gmath::faraday = gmath::elementary_charge * gmath::avogadro;
const double gmath::electron_volt = gmath::faraday / gmath::kilo;
const double gmath::eps0 = 5.72765E-4;
const double gmath::speed_of_light = 2.9979245800E05;
const double gmath::four_pi_eps_i = 1.0 / (4.0 * gmath::pi * gmath::eps0);
const double gmath::euler = 2.718281828459;
const double gmath::hbar = 1.054571596e-34;
